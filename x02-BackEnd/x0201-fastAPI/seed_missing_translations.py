def main():
    print("=" * 60)
    print("  Seed Missing Translations")
    print("=" * 60)

    conn = sqlite3.connect(DB_PATH)
    count = 0
    for key, (en_val, th_val) in MISSING.items():
        for locale, value in [("en", en_val), ("th", th_val)]:
            conn.execute("""
                INSERT INTO translations (key, locale, value, updated_at)
                VALUES (?, ?, ?, CURRENT_TIMESTAMP)
                ON CONFLICT(key, locale)
                DO UPDATE SET value = excluded.value, updated_at = CURRENT_TIMESTAMP
            """, (key, locale, value))
            count += 1
    conn.commit()

    total = conn.execute("SELECT COUNT(*) FROM translations").fetchone()[0]
    en_count = conn.execute("SELECT COUNT(*) FROM translations WHERE locale='en'").fetchone()[0]
    th_count = conn.execute("SELECT COUNT(*) FROM translations WHERE locale='th'").fetchone()[0]
    conn.close()

    print(f"\n✅ Upserted: {count} entries ({len(MISSING)} keys × 2 locales)")
    print(f"   Total in DB: {total}")
    print(f"   EN: {en_count}")
    print(f"   TH: {th_count}")
    print("=" * 60)


if __name__ == "__main__":
    main()
